// Function to format work experience content
function formatWorkExperience(exp) {
    let content = `<p><strong>Company:</strong> ${exp.company}</p>`;
    if (exp.project) {
        content += `<p><strong>Project:</strong> ${exp.project}</p>`;
    }
    if (exp.skills) {
        content += `<p><strong>Skills:</strong> ${exp.skills}</p>`;
    }
    content += `<p><strong>Key Responsibilities:</strong></p><ul>`;
    exp.responsibilities.forEach(resp => {
        content += `<li>${resp}</li>`;
    });
    content += `</ul>`;
    if (exp.accomplishments && exp.accomplishments.length > 0) {
        content += `<p><strong>Accomplishments:</strong></p><ul>`;
        exp.accomplishments.forEach(acc => {
            content += `<li>${acc}</li>`;
        });
        content += `</ul>`;
    }
    return content;
}

// Function to format project content
function formatProject(project) {
    let content = '';
    if (project.description) {
        content += `<p><strong>Description:</strong> ${project.description}</p>`;
    }
    if (project.accomplishments && project.accomplishments.length > 0) {
        content += `<p><strong>Accomplishments:</strong></p><ul>`;
        project.accomplishments.forEach(acc => {
            content += `<li>${acc}</li>`;
        });
        content += `</ul>`;
    }
    if (project.technologies && project.technologies.length > 0) {
        content += `<p><strong>Technologies Used:</strong> ${project.technologies.join(', ')}</p>`;
    }
    return content;
}
